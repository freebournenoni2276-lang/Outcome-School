# Introduce the program to the user
print("Hello and welcome to my program!")
# Ask the user for their name
name = input("Enter your name: ")
# Ask the user for two numbers
number_1 = int(input("Enter a number: "))
number_2 = int(input("Enter another number: "))
# Calculate the sum and product of the two numbers
sum = number_1 + number_2
product = number_1 * number_2
# Print the sum and product of the two numbers
print("Hello " + name + ", the sum of " + str(number_1) + " and " + str(number_2) + " is " + str(sum) + ", and their product is " + str(product) + ".")